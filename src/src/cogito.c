int cogito_gst_poll_event(void) {
#if defined(COGITO_HAS_GSTREAMER)
  if (cogito_gst_pending_event != 0) {
    int ev = cogito_gst_pending_event;
    cogito_gst_pending_event = 0;
    return ev;
  }
  if (!cogito_gst_bus)
    return 0;

  for (;;) {
    GstMessage *msg = gst_bus_pop(cogito_gst_bus);
    if (!msg)
      return 0;
    int ev = 0;
    switch (GST_MESSAGE_TYPE(msg)) {
    case GST_MESSAGE_EOS:
      ev = 1;
      break;
    case GST_MESSAGE_ERROR: {
      GError *err = NULL;
      gchar *debug = NULL;
      gst_message_parse_error(msg, &err, &debug);
      if (err && err->message)
        cogito_gst_set_error_msg(err->message);
      else
        cogito_gst_set_error_msg("gstreamer playback error");
      if (err)
        g_error_free(err);
      if (debug)
        g_free(debug);
      ev = 2;
      break;
    }
    case GST_MESSAGE_TAG: {
      GstTagList *tags = NULL;
      gst_message_parse_tag(msg, &tags);
      if (tags) {
        GstSample *sample = NULL;
        if (cogito_gst_try_sample_from_tags(tags, &sample)) {
          if (cogito_gst_store_cover_sample(sample)) {
            ev = 3;
          }
          gst_sample_unref(sample);
        }
        gchar *str_val = NULL;
        if (gst_tag_list_get_string(tags, GST_TAG_TITLE, &str_val)) {
          if (str_val && str_val[0])
            snprintf(cogito_gst_tag_title, sizeof(cogito_gst_tag_title),
                     "%s", str_val);
          g_free(str_val);
          str_val = NULL;
          ev = 3;
        }
        if (gst_tag_list_get_string(tags, GST_TAG_ARTIST, &str_val)) {
          if (str_val && str_val[0])
            snprintf(cogito_gst_tag_artist, sizeof(cogito_gst_tag_artist),
                     "%s", str_val);
          g_free(str_val);
          str_val = NULL;
          ev = 3;
        }
        if (gst_tag_list_get_string(tags, GST_TAG_ALBUM, &str_val)) {
          if (str_val && str_val[0])
            snprintf(cogito_gst_tag_album, sizeof(cogito_gst_tag_album),
                     "%s", str_val);
          g_free(str_val);
          str_val = NULL;
          ev = 3;
        }
        {
          guint track_num = 0;
          if (gst_tag_list_get_uint(tags, GST_TAG_TRACK_NUMBER, &track_num)) {
            cogito_gst_tag_track_number = (int)track_num;
            ev = 3;
          }
        }
        gst_tag_list_unref(tags);
      }
      break;
    }
    default:
      break;
    }
    gst_message_unref(msg);
    if (ev != 0)
      return ev;
  }
#else
  return 0;
#endif
}

